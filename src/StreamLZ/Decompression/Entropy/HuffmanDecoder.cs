    /// <summary>
    /// Bit-reversal permutation of a 2048-element byte array.
    /// Rearranges elements so that the 11-bit index has its bits flipped.
    /// Uses SSE2 intrinsics for fast 8-way interleave.
    /// </summary>
    [SkipLocalsInit]
    public static void ReverseBitsArray2048(byte* input, byte* output)
    {
        ReadOnlySpan<byte> offsets =
        [
            0x00, 0x80, 0x40, 0xC0, 0x20, 0xA0, 0x60, 0xE0,
            0x10, 0x90, 0x50, 0xD0, 0x30, 0xB0, 0x70, 0xF0,
            0x08, 0x88, 0x48, 0xC8, 0x28, 0xA8, 0x68, 0xE8,
            0x18, 0x98, 0x58, 0xD8, 0x38, 0xB8, 0x78, 0xF8
        ];

        if (Sse2.IsSupported)
        {
            for (int i = 0; i < 32; i++)
            {
                int j = offsets[i];

                var t0 = Sse2.UnpackLow(
                    Sse2.LoadScalarVector128((long*)(input + j)).AsByte(),
                    Sse2.LoadScalarVector128((long*)(input + j + 256)).AsByte());
                var t1 = Sse2.UnpackLow(
                    Sse2.LoadScalarVector128((long*)(input + j + 512)).AsByte(),
                    Sse2.LoadScalarVector128((long*)(input + j + 768)).AsByte());
                var t2 = Sse2.UnpackLow(
                    Sse2.LoadScalarVector128((long*)(input + j + 1024)).AsByte(),
                    Sse2.LoadScalarVector128((long*)(input + j + 1280)).AsByte());
                var t3 = Sse2.UnpackLow(
                    Sse2.LoadScalarVector128((long*)(input + j + 1536)).AsByte(),
                    Sse2.LoadScalarVector128((long*)(input + j + 1792)).AsByte());

                var s0 = Sse2.UnpackLow(t0, t1);
                var s1 = Sse2.UnpackLow(t2, t3);
                var s2 = Sse2.UnpackHigh(t0, t1);
                var s3 = Sse2.UnpackHigh(t2, t3);

                t0 = Sse2.UnpackLow(s0, s1);
                t1 = Sse2.UnpackLow(s2, s3);
                t2 = Sse2.UnpackHigh(s0, s1);
                t3 = Sse2.UnpackHigh(s2, s3);

                // Store low 8 bytes and high 8 bytes to different offsets
                Sse2.StoreScalar((long*)(output), t0.AsInt64());
                Sse2.StoreScalar((long*)(output + 1024), Sse2.UnpackHigh(t0.AsInt64(), t0.AsInt64()).AsByte().AsInt64());
                Sse2.StoreScalar((long*)(output + 256), t1.AsInt64());
                Sse2.StoreScalar((long*)(output + 1280), Sse2.UnpackHigh(t1.AsInt64(), t1.AsInt64()).AsByte().AsInt64());
                Sse2.StoreScalar((long*)(output + 512), t2.AsInt64());
                Sse2.StoreScalar((long*)(output + 1536), Sse2.UnpackHigh(t2.AsInt64(), t2.AsInt64()).AsByte().AsInt64());
                Sse2.StoreScalar((long*)(output + 768), t3.AsInt64());
                Sse2.StoreScalar((long*)(output + 1792), Sse2.UnpackHigh(t3.AsInt64(), t3.AsInt64()).AsByte().AsInt64());

                output += 8;
            }
        }
        else if (AdvSimd.Arm64.IsSupported)
        {
            // ARM NEON: ZipLow/ZipHigh are the NEON equivalents of SSE2 UnpackLow/UnpackHigh
            for (int i = 0; i < 32; i++)
            {
                int j = offsets[i];

                var t0 = AdvSimd.Arm64.ZipLow(
                    Vector128.Create(*(long*)(input + j), 0).AsByte(),
                    Vector128.Create(*(long*)(input + j + 256), 0).AsByte());
                var t1 = AdvSimd.Arm64.ZipLow(
                    Vector128.Create(*(long*)(input + j + 512), 0).AsByte(),
                    Vector128.Create(*(long*)(input + j + 768), 0).AsByte());
                var t2 = AdvSimd.Arm64.ZipLow(
                    Vector128.Create(*(long*)(input + j + 1024), 0).AsByte(),
                    Vector128.Create(*(long*)(input + j + 1280), 0).AsByte());
                var t3 = AdvSimd.Arm64.ZipLow(
                    Vector128.Create(*(long*)(input + j + 1536), 0).AsByte(),
                    Vector128.Create(*(long*)(input + j + 1792), 0).AsByte());

                var s0 = AdvSimd.Arm64.ZipLow(t0.AsInt16(), t1.AsInt16()).AsByte();
                var s1 = AdvSimd.Arm64.ZipLow(t2.AsInt16(), t3.AsInt16()).AsByte();
                var s2 = AdvSimd.Arm64.ZipHigh(t0.AsInt16(), t1.AsInt16()).AsByte();
                var s3 = AdvSimd.Arm64.ZipHigh(t2.AsInt16(), t3.AsInt16()).AsByte();

                t0 = AdvSimd.Arm64.ZipLow(s0.AsInt32(), s1.AsInt32()).AsByte();
                t1 = AdvSimd.Arm64.ZipLow(s2.AsInt32(), s3.AsInt32()).AsByte();
                t2 = AdvSimd.Arm64.ZipHigh(s0.AsInt32(), s1.AsInt32()).AsByte();
                t3 = AdvSimd.Arm64.ZipHigh(s2.AsInt32(), s3.AsInt32()).AsByte();

                *(long*)(output) = t0.AsInt64().GetElement(0);
                *(long*)(output + 1024) = t0.AsInt64().GetElement(1);
                *(long*)(output + 256) = t1.AsInt64().GetElement(0);
                *(long*)(output + 1280) = t1.AsInt64().GetElement(1);
                *(long*)(output + 512) = t2.AsInt64().GetElement(0);
                *(long*)(output + 1536) = t2.AsInt64().GetElement(1);
                *(long*)(output + 768) = t3.AsInt64().GetElement(0);
                *(long*)(output + 1792) = t3.AsInt64().GetElement(1);

                output += 8;
            }
        }
        else
        {
            // Scalar fallback
            for (int i = 0; i < 2048; i++)
            {
                // Reverse the 11 bits of i
                int rev = 0;
                int val = i;
                for (int b = 0; b < 11; b++)
                {
                    rev = (rev << 1) | (val & 1);
                    val >>= 1;
                }
                output[rev] = input[i];
            }
        }
    }

