    #region BitReader helpers

    [MethodImpl(MethodImplOptions.AggressiveInlining)]
    private static int CountLeadingZeros(uint bits)
    {
        return BitOperations.LeadingZeroCount(bits);
    }

    [MethodImpl(MethodImplOptions.AggressiveInlining)]
    public static void BitReader_Refill(ref BitReaderState bits)
    {
        while (bits.Bitpos > 0)
        {
            bits.Bits |= (uint)(bits.P < bits.PEnd ? *bits.P : 0) << bits.Bitpos;
            bits.Bitpos -= 8;
            bits.P++;
        }
    }

    [MethodImpl(MethodImplOptions.AggressiveInlining)]
    public static int BitReader_ReadBit(ref BitReaderState bits)
    {
        BitReader_Refill(ref bits);
        int r = (int)(bits.Bits >> 31);
        bits.Bits <<= 1;
        bits.Bitpos += 1;
        return r;
    }

    [MethodImpl(MethodImplOptions.AggressiveInlining)]
    public static int BitReader_ReadBitNoRefill(ref BitReaderState bits)
    {
        int r = (int)(bits.Bits >> 31);
        bits.Bits <<= 1;
        bits.Bitpos += 1;
        return r;
    }

    [MethodImpl(MethodImplOptions.AggressiveInlining)]
    public static int BitReader_ReadBitsNoRefill(ref BitReaderState bits, int n)
    {
        int r = (int)(bits.Bits >> (32 - n));
        bits.Bits <<= n;
        bits.Bitpos += n;
        return r;
    }

    [MethodImpl(MethodImplOptions.AggressiveInlining)]
    public static int BitReader_ReadBitsNoRefillZero(ref BitReaderState bits, int n)
    {
        int r = (int)(bits.Bits >> 1 >> (31 - n));
        bits.Bits <<= n;
        bits.Bitpos += n;
        return r;
    }

