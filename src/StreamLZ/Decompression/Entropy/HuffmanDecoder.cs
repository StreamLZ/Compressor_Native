    #region BitReader state struct

    /// <summary>
    /// Minimal mutable bit reader state used throughout Huffman and TANS decoding.
    /// </summary>
    /// <remarks>
    /// <para>
    /// This struct exists separately from <see cref="Common.BitReader"/> for two reasons:
    /// </para>
    /// <list type="number">
    ///   <item>
    ///     <b>API pattern:</b> The Huffman/TANS decoders use a static-method API
    ///     (e.g. <c>BitReader_Refill(ref BitReaderState)</c>) rather than instance methods.
    ///     This keeps the hot decode loops free of indirect dispatch and allows the JIT to
    ///     inline the state into registers without struct-copy overhead.
    ///   </item>
    ///   <item>
    ///     <b>Minimal surface:</b> <see cref="Common.BitReader"/> carries additional methods
    ///     (ReadDistance, ReadLength, ReadGamma, etc.) needed by the LZ offset/length
    ///     unpacking layer. BitReaderState is intentionally stripped down to just the four
    ///     fields required by the bulk Huffman symbol decode loop, keeping the struct small
    ///     and cache-friendly.
    ///   </item>
    /// </list>
    /// <para>
    /// Note: the field is named <c>Bitpos</c> (lowercase 'p') here vs <c>BitPos</c> in
    /// Common.BitReader. Both use the same MSB-first convention where Bitpos/BitPos starts
    /// at 24 and refill loads bytes until it drops to 0 or below.
    /// </para>
    /// </remarks>
    [StructLayout(LayoutKind.Sequential)]
    public unsafe struct BitReaderState
    {
        /// <summary>Current read position in the byte stream.</summary>
        public byte* P;
        /// <summary>End-of-stream sentinel (forward) or start sentinel (backward).</summary>
        public byte* PEnd;
        /// <summary>Accumulated bits; most-significant bits are consumed first.</summary>
        public uint Bits;
        /// <summary>Next byte lands at this bit position; &lt;= 0 means at least 24 valid bits.</summary>
        public int Bitpos;
    }

    #endregion

    #region ReadFluff

    /// <summary>
    /// Reads the "fluff" value that encodes range information for fewer-than-256 symbol alphabets.
    /// </summary>
    public static int BitReader_ReadFluff(ref BitReaderState bits, int numSymbols)
    {
        if (numSymbols == 256)
        {
            return 0;
        }

        int x = 257 - numSymbols;
        if (x > numSymbols)
        {
            x = numSymbols;
        }

        x *= 2;

        uint y = (uint)BitOperations.Log2((uint)(x - 1)) + 1;

        uint v = bits.Bits >> (int)(32 - y);
        uint z = (1u << (int)y) - (uint)x;

        if ((v >> 1) >= z)
        {
            bits.Bits <<= (int)y;
            bits.Bitpos += (int)y;
            return (int)(v - z);
        }
        else
        {
            bits.Bits <<= (int)(y - 1);
            bits.Bitpos += (int)(y - 1);
            return (int)(v >> 1);
        }
    }

