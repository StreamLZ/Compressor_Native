//! Lightweight bit-reader types and Golomb-Rice decoding primitives.
//!
//! Shared by both the Huffman and tANS decoders for frequency-table
//! reading.  Kept separate from `io/BitReader.zig` (which carries
//! distance/length/gamma methods) so the entropy hot paths pull in
//! only the minimal API they need.

const std = @import("std");

// ────────────────────────────────────────────────────────────
//  Data structures
// ────────────────────────────────────────────────────────────

/// MSB-first, forward-refill bit reader for table headers.
pub const BitReaderState = struct {
    p: [*]const u8,
    p_end: [*]const u8,
    bits: u32,
    bit_pos: i32,
};

/// Secondary byte-aligned reader used by Golomb-Rice decoding.
pub const BitReader2 = struct {
    p: [*]const u8,
    p_end: [*]const u8,
    bit_pos: u32,
};

/// Symbol range produced by `huffConvertToRanges`.
pub const HuffRange = struct {
    symbol: u16,
    num: u16,
};

pub const DecodeError = error{
    CorruptStream,
    SourceTruncated,
    OutputTruncated,
    BadCodeLengthFormat,
    InvalidCodeLength,
    InvalidRanges,
    LutConstructionFailed,
    StreamMismatch,
};

