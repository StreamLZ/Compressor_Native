// ────────────────────────────────────────────────────────────
//  BitReaderState helpers (MSB-first, forward refill)
// ────────────────────────────────────────────────────────────

pub inline fn bitReaderRefill(br: *BitReaderState) void {
    while (br.bit_pos > 0) {
        const byte: u32 = if (@intFromPtr(br.p) < @intFromPtr(br.p_end)) br.p[0] else 0;
        br.bits |= byte << @intCast(br.bit_pos);
        br.bit_pos -= 8;
        br.p += 1;
    }
}

pub inline fn bitReaderReadBit(br: *BitReaderState) u32 {
    bitReaderRefill(br);
    const r = br.bits >> 31;
    br.bits <<= 1;
    br.bit_pos += 1;
    return r;
}

pub inline fn bitReaderReadBitNoRefill(br: *BitReaderState) u32 {
    const r = br.bits >> 31;
    br.bits <<= 1;
    br.bit_pos += 1;
    return r;
}

/// Reads `n` bits (n >= 1) without refilling.
pub inline fn bitReaderReadBitsNoRefill(br: *BitReaderState, n: u5) u32 {
    std.debug.assert(n >= 1);
    const r = br.bits >> @intCast(32 - @as(u6, n));
    br.bits <<= n;
    br.bit_pos += n;
    return r;
}

/// Reads `n` bits without refilling; `n` may be zero. Uses the two-shift
/// trick to dodge the undefined shift-by-32 case.
pub inline fn bitReaderReadBitsNoRefillZero(br: *BitReaderState, n: u6) u32 {
    if (n == 0) return 0;
    const r = (br.bits >> 1) >> @intCast(31 - n);
    br.bits <<= @intCast(n);
    br.bit_pos += @intCast(n);
    return r;
}

/// Reads the "fluff" encoding for alphabets with fewer than 256 symbols.
pub fn bitReaderReadFluff(br: *BitReaderState, num_symbols: u32) u32 {
    if (num_symbols == 256) return 0;
    var x: u32 = 257 - num_symbols;
    if (x > num_symbols) x = num_symbols;
    x *= 2;

    const y: u32 = std.math.log2_int(u32, x - 1) + 1;
    const v: u32 = br.bits >> @intCast(32 - y);
    const z: u32 = (@as(u32, 1) << @intCast(y)) - x;

    if ((v >> 1) >= z) {
        br.bits <<= @intCast(y);
        br.bit_pos += @intCast(y);
        return v - z;
    } else {
        br.bits <<= @intCast(y - 1);
        br.bit_pos += @intCast(y - 1);
        return v >> 1;
    }
}

// ────────────────────────────────────────────────────────────
//  Golomb-Rice decoding
// ────────────────────────────────────────────────────────────

/// Decodes Golomb-Rice run lengths from a bit stream.
pub fn decodeGolombRiceLengths(dst_buf: [*]u8, size: usize, br: *BitReader2) DecodeError!void {
    var p = br.p;
    const p_end = br.p_end;
    var dst = dst_buf;
    const dst_end = dst_buf + size;

    if (@intFromPtr(p) >= @intFromPtr(p_end)) return error.SourceTruncated;

    var count: i32 = -@as(i32, @intCast(br.bit_pos));
    const initial_mask: u32 = @as(u32, 255) >> @intCast(br.bit_pos);
    var v: u32 = p[0] & initial_mask;
    p += 1;

    while (true) {
        if (v == 0) {
            count += 8;
        } else {
            const x = k_rice_code_bits2_value[v];
            // Store count + low-nibble symbols at dst[0..4]. `count` is small
            // (may be slightly negative); byte-wise the math must wrap.
            const lo: u32 = @as(u32, @bitCast(count)) +% (x & 0x0F0F_0F0F);
            const hi: u32 = (x >> 4) & 0x0F0F_0F0F;
            std.mem.writeInt(u32, dst[0..4], lo, .little);
            std.mem.writeInt(u32, dst[4..8], hi, .little);
            dst += k_rice_code_bits2_len[v];
            if (@intFromPtr(dst) >= @intFromPtr(dst_end)) break;
            count = @intCast(x >> 28);
        }
        if (@intFromPtr(p) >= @intFromPtr(p_end)) return error.SourceTruncated;
        v = p[0];
        p += 1;
    }

    // Overshoot correction: step back by dropping trailing set bits from v.
    if (@intFromPtr(dst) > @intFromPtr(dst_end)) {
        var n: usize = @intFromPtr(dst) - @intFromPtr(dst_end);
        while (n != 0) : (n -= 1) {
            v &= v - 1;
        }
    }

    var bit_pos: u32 = 0;
    if ((v & 1) == 0) {
        p -= 1;
        const q: u32 = @ctz(v);
        bit_pos = 8 - q;
    }
    br.p = p;
    br.bit_pos = bit_pos;
}

/// Decodes additional Golomb-Rice precision bits (1, 2, or 3) and merges them
/// into the length array. Uses SIMD-style bit unpacking (u64 bit manipulation
/// tricks).
pub fn decodeGolombRiceBits(dst_buf: [*]u8, size: usize, bitcount: u32, br: *BitReader2) DecodeError!void {
    if (bitcount == 0) return;

    const dst_end = dst_buf + size;
    var p = br.p;
    const p_end = br.p_end;
    const bit_pos = br.bit_pos;

    const bits_required: u32 = @intCast(bit_pos + bitcount * @as(u32, @intCast(size)));
    const bytes_required: usize = (bits_required + 7) >> 3;
    if (bytes_required > @intFromPtr(p_end) - @intFromPtr(p)) return error.SourceTruncated;

    br.p = p + (bits_required >> 3);
    br.bit_pos = bits_required & 7;

    // Caller must guarantee 8-byte padding past dst_end.
    const bak: u64 = std.mem.readInt(u64, dst_end[0..8], .little);
    var dst = dst_buf;

    switch (bitcount) {
        1 => {
            while (@intFromPtr(dst) < @intFromPtr(dst_end)) {
                const raw: u32 = @byteSwap(std.mem.readInt(u32, p[0..4], .little));
                var bits: u64 = @as(u8, @truncate(raw >> @intCast(24 - bit_pos)));
                p += 1;
                bits = (bits | (bits << 28)) & 0x0F0000_000F;
                bits = (bits | (bits << 14)) & 0x0003_0003_0003_0003;
                bits = (bits | (bits << 7)) & 0x0101_0101_0101_0101;
                const prev = std.mem.readInt(u64, dst[0..8], .little);
                const result = prev * 2 + @byteSwap(bits);
                std.mem.writeInt(u64, dst[0..8], result, .little);
                dst += 8;
            }
        },
        2 => {
            while (@intFromPtr(dst) < @intFromPtr(dst_end)) {
                const raw: u32 = @byteSwap(std.mem.readInt(u32, p[0..4], .little));
                var bits: u64 = @as(u16, @truncate(raw >> @intCast(16 - bit_pos)));
                p += 2;
                bits = (bits | (bits << 24)) & 0x00FF_0000_00FF;
                bits = (bits | (bits << 12)) & 0x000F_000F_000F_000F;
                bits = (bits | (bits << 6)) & 0x0303_0303_0303_0303;
                const prev = std.mem.readInt(u64, dst[0..8], .little);
                const result = prev * 4 + @byteSwap(bits);
                std.mem.writeInt(u64, dst[0..8], result, .little);
                dst += 8;
            }
        },
        else => { // bitcount == 3
            while (@intFromPtr(dst) < @intFromPtr(dst_end)) {
                const raw: u32 = @byteSwap(std.mem.readInt(u32, p[0..4], .little));
                var bits: u64 = (raw >> @intCast(8 - bit_pos)) & 0xFFFFFF;
                p += 3;
                bits = (bits | (bits << 20)) & 0x0FFF_0000_0FFF;
                bits = (bits | (bits << 10)) & 0x003F_003F_003F_003F;
                bits = (bits | (bits << 5)) & 0x0707_0707_0707_0707;
                const prev = std.mem.readInt(u64, dst[0..8], .little);
                const result = prev * 8 + @byteSwap(bits);
                std.mem.writeInt(u64, dst[0..8], result, .little);
                dst += 8;
            }
        },
    }

    std.mem.writeInt(u64, dst_end[0..8], bak, .little);
}

// ────────────────────────────────────────────────────────────
//  Range conversion
// ────────────────────────────────────────────────────────────

pub fn huffConvertToRanges(
    ranges: [*]HuffRange,
    num_symbols: u32,
    p_fluff: u32,
    symlen_in: [*]const u8,
    br: *BitReaderState,
) DecodeError!u32 {
    const num_ranges: u32 = p_fluff >> 1;
    var sym_idx: i32 = 0;
    var symlen = symlen_in;

    if ((p_fluff & 1) != 0) {
        bitReaderRefill(br);
        const v: u32 = symlen[0];
        symlen += 1;
        if (v >= 8) return error.BadCodeLengthFormat;
        const extra: u5 = @intCast(v + 1);
        sym_idx = @intCast(bitReaderReadBitsNoRefill(br, extra) + (@as(u32, 1) << extra) - 1);
    }

    var syms_used: u32 = 0;
    var i: u32 = 0;
    while (i < num_ranges) : (i += 1) {
        bitReaderRefill(br);
        const v0: u32 = symlen[0];
        if (v0 >= 9) return error.BadCodeLengthFormat;
        const v0_u5: u6 = @intCast(v0);
        const num = bitReaderReadBitsNoRefillZero(br, v0_u5) + (@as(u32, 1) << @intCast(v0));
        const v1: u32 = symlen[1];
        if (v1 >= 8) return error.BadCodeLengthFormat;
        const v1_u5: u5 = @intCast(v1 + 1);
        const space = bitReaderReadBitsNoRefill(br, v1_u5) + (@as(u32, 1) << v1_u5) - 1;
        ranges[i] = .{ .symbol = @intCast(sym_idx), .num = @intCast(num) };
        syms_used += num;
        sym_idx += @intCast(num + space);
        symlen += 2;
    }

    if (sym_idx >= 256 or syms_used >= num_symbols or
        @as(u32, @intCast(sym_idx)) + num_symbols - syms_used > 256)
    {
        return error.InvalidRanges;
    }
    ranges[num_ranges] = .{ .symbol = @intCast(sym_idx), .num = @intCast(num_symbols - syms_used) };
    return num_ranges + 1;
}

